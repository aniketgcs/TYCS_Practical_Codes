start="Arad"
goal="Fagaras"
result=''

def BFS(source, cityq, visitedq):
    global result
    if source==start:
        result=result+' '+source
    for x in cities[source].keys():
        if x==goal:
            result=result+' '+x
            return
        if x not in cityq.queue and x not in visitedq.queue:
            cityq.put(x)
            result=result+' '+x
    visitedq.put(source)
    BFS(cityq.get(),cityq,visitedq)

def main():
    cityq=Q.Queue()
    visitedq=Q.Queue()
    BFS(start, cityq, visitedq)
    print("BFS Traversal from ",start," to ",goal," is: ")
    print(result)

